int screen_open(quasar_screen_t *scr, const char *ignored)
{
	(void)ignored;
	memset(scr, 0, sizeof(*scr));
	scr->fb_fd = -1;
	scr->fb_mmap = NULL;

	char fb_path[32];
	struct fb_fix_screeninfo fix;
	struct fb_var_screeninfo var;

	for (int i = 0; i < 8; i++) {
		snprintf(fb_path, sizeof(fb_path), "/dev/fb%d", i);
		int fd = open(fb_path, O_RDWR);
		if (fd < 0)
			continue;

		if (ioctl(fd, FBIOGET_FSCREENINFO, &fix) == 0 &&
		    strcmp(fix.id, ATRI_FB_ID) == 0 &&
		    ioctl(fd, FBIOGET_VSCREENINFO, &var) == 0) {
			scr->fb_len = fix.smem_len > 0 ? fix.smem_len : SCREEN_BYTES;
			void *map = mmap(NULL, scr->fb_len, PROT_READ | PROT_WRITE,
					 MAP_SHARED, fd, 0);
			if (map == MAP_FAILED) {
				perror("mmap fb");
				close(fd);
				return -1;
			}
			scr->fb_fd = fd;
			scr->fb_mmap = map;
			return 0;
		}
		close(fd);
	}

	fprintf(stderr, "atri_led_panel fbdev not found\n");
	return -1;
}

void screen_close(quasar_screen_t *scr)
{
	if (scr->fb_mmap) {
		munmap(scr->fb_mmap, scr->fb_len);
		scr->fb_mmap = NULL;
	}
	if (scr->fb_fd >= 0) {
		close(scr->fb_fd);
		scr->fb_fd = -1;
	}
}

void screen_reset(quasar_screen_t *scr)
{
	(void)scr;
}

void screen_clear(quasar_screen_t *scr)
{
	if (scr->fb_mmap)
		memset(scr->fb_mmap, 0, SCREEN_BYTES);
}

void screen_pixel(quasar_screen_t *scr, int x, int y, int on)
{
	if (x < 0 || x >= SCREEN_WIDTH || y < 0 || y >= SCREEN_HEIGHT)
		return;
	if (!scr->fb_mmap)
		return;
	scr->fb_mmap[y * SCREEN_WIDTH + x] = on ? 0xff : 0x00;
}

void screen_rect(quasar_screen_t *scr, int x, int y, int w, int h, int on)
{
	for (int row = y; row < y + h && row < SCREEN_HEIGHT; row++)
		for (int col = x; col < x + w && col < SCREEN_WIDTH; col++)
			screen_pixel(scr, col, row, on);
}

static int abs_int(int x) { return x < 0 ? -x : x; }

void screen_line(quasar_screen_t *scr, int x0, int y0, int x1, int y1, int on)
{
	int dx = abs_int(x1 - x0), sx = x0 < x1 ? 1 : -1;
	int dy = -abs_int(y1 - y0), sy = y0 < y1 ? 1 : -1;
	int err = dx + dy, e2;

	while (1) {
		screen_pixel(scr, x0, y0, on);
		if (x0 == x1 && y0 == y1)
			break;
		e2 = 2 * err;
		if (e2 >= dy) { err += dy; x0 += sx; }
		if (e2 <= dx) { err += dx; y0 += sy; }
	}
}

void screen_text(quasar_screen_t *scr, int x, int y, const char *text, int on)
{
	int orig_x = x;
	for (const char *p = text; *p && x < SCREEN_WIDTH; p++) {
		unsigned char ch = (unsigned char)*p;
		if (ch < 32 || ch > 127) ch = '?';
		ch -= 32;
		for (int col = 0; col < 5 && x + col < SCREEN_WIDTH; col++) {
			uint8_t glyph = font5x7[ch][col];
			for (int row = 0; row < 7 && y + row < SCREEN_HEIGHT; row++) {
				if (glyph & (1 << (6 - row)))
					screen_pixel(scr, x + col, y + row, on);
			}
		}
		x += 6;
		if (x > SCREEN_WIDTH - 5) { x = orig_x; y += 8; }
	}
}

void screen_flush(quasar_screen_t *scr)
{
	if (scr->fb_fd < 0)
		return;
	int blank = FB_BLANK_UNBLANK;
	ioctl(scr->fb_fd, FBIOBLANK, blank);
}

